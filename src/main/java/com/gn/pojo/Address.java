package com.gn.pojo;

public class Address {

	private String yiji;
	
	private String erji;
	
	private String sanji;

	public String getYiji() {
		return yiji;
	}

	public void setYiji(String yiji) {
		this.yiji = yiji;
	}

	public String getErji() {
		return erji;
	}

	public void setErji(String erji) {
		this.erji = erji;
	}

	public String getSanji() {
		return sanji;
	}

	public void setSanji(String sanji) {
		this.sanji = sanji;
	}
	
	
}
